package com.example.demo.entity;

public class Team {
	private Long id;
	private String name;

	public Long id() {
		return id;
	}

	public String name() {
		return name;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName(String name) {
		return name;
	}

}
